DEVICE = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")

# --- 1. Model Architecture (Matching glowing-valley-22 checkpoint) ---
class TransformerModel(nn.Module):
    def __init__(self, vocab_size, n_embd, n_heads, n_layers, max_seq_length, dropout, dim_feedforward):
        super(TransformerModel, self).__init__()
        self.n_embd = n_embd
        self.max_seq_length = max_seq_length
        self.embedding = nn.Embedding(vocab_size + 1, n_embd)
        
        # Positional Encoding
        position = torch.arange(max_seq_length).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, n_embd, 2) * -(math.log(10000.0) / n_embd))
        pos_enc = torch.zeros(max_seq_length, n_embd)
        pos_enc[:, 0::2] = torch.sin(position * div_term)
        pos_enc[:, 1::2] = torch.cos(position * div_term)
        
        # Register with extra dimension [1, max_seq_length, n_embd] to match checkpoint
        self.register_buffer('pos_embedding', pos_enc.unsqueeze(0))

        self.transformer = nn.TransformerEncoder(
            nn.TransformerEncoderLayer(
                d_model=n_embd, 
                nhead=n_heads, 
                dim_feedforward=dim_feedforward, 
                dropout=dropout, 
                batch_first=True
            ),
            num_layers=n_layers)
        self.linear = nn.Linear(n_embd, vocab_size)

    def forward(self, tokens):
        # Safety net: Truncate sequence if it exceeds the maximum allowed length
        if tokens.size(1) > self.max_seq_length:
            tokens = tokens[:, -self.max_seq_length:]
            
        token_embeddings = self.embedding(tokens)
        position_embeddings = self.pos_embedding[:, :tokens.size(1), :].to(tokens.device)
        embeddings = token_embeddings + position_embeddings

        seq_length = tokens.size(1)
        causal_mask = torch.triu(torch.ones(seq_length, seq_length), diagonal=1).to(tokens.device)
        causal_mask = causal_mask.masked_fill(causal_mask == 1, float('-inf'))

        transformer_output = self.transformer(embeddings, mask=causal_mask)
        logits = self.linear(transformer_output)
        return logits

# --- 2. Simulation Utilities ---
GLUCOSE_LIMIT = 461

def sample_from_logits(logits, k=3):
    # Apply top-k sampling to ensure diversity and avoid repetitive loops
    values, _ = torch.topk(logits, k)
    min_values = values[:, -1].unsqueeze(1).expand_as(logits)
    filtered_logits = torch.where(logits < min_values, torch.full_like(logits, float('-inf')), logits)
    probs = torch.nn.functional.softmax(filtered_logits, dim=-1)
    return torch.multinomial(probs, 1)

def run_simulation(model, history_tokens, menu_per_step, forecast_steps):
    # Initialize the sequence with the full subject history
    generated_seq = torch.tensor(history_tokens, dtype=torch.long).unsqueeze(0).to(DEVICE)
    predictions = []
    
    # Define the sliding window size based on model limits
    window_size = 1200 

    for t in tqdm(range(forecast_steps), desc="Predicting Week 2"):
        with torch.no_grad():
            # Inject food tokens if scheduled for the current time step
            if t in menu_per_step:
                meal_tokens = menu_per_step[t]
                meal_tensor = torch.tensor([meal_tokens], dtype=torch.long).to(DEVICE)
                generated_seq = torch.cat([generated_seq, meal_tensor], dim=1)
            
            # Extract the relevant sliding window
            input_seq = generated_seq[:, -window_size:]
            
            # Predict the next token
            logits = model(input_seq)[:, -1, :]
            logits[:, GLUCOSE_LIMIT:] = float('-inf') # Restrict prediction to glucose tokens
            
            next_token = sample_from_logits(logits)
            
            # Append the predicted token back into the main sequence
            generated_seq = torch.cat([generated_seq, next_token], dim=1)
            predictions.append(next_token.item())
            
    return predictions

